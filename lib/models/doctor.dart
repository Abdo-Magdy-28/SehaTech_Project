class Doctor {
  final String name;
  final String job;
  final String hospital;
  final String image;
  final double rate;
  final String beginDate;
  final String endDate;

  Doctor({
    required this.name,
    required this.job,
    required this.hospital,
    required this.image,
    required this.rate,
    required this.beginDate,
    required this.endDate,
  });
}
